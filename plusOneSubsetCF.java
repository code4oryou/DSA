import java.util.*;

public class plusOneSubsetCF {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t-- > 0) {
            int n = sc.nextInt();
            int min = Integer.MAX_VALUE;
            int max = Integer.MIN_VALUE;
            for (int i = 0; i < n; i++) {
                int p = sc.nextInt();
                min = Math.min(min, p);
                max = Math.max(max, p);
            }
            System.out.println(max - min);
        }
        sc.close();
    }
}
