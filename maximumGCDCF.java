import java.util.*;

public class maximumGCDCF {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int t = in.nextInt();
        while (t-- > 0) {
            Long n = in.nextLong();
            System.out.println(n / 2);
        }
        in.close();
    }
}
